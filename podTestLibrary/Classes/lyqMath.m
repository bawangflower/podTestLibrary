#import "lyqMath.h"

@implementation lyqMath

+ (NSInteger)addWithA:(NSInteger)A andB :(NSInteger)B {
    NSInteger C = A + B;
    return C;
}

+ (NSInteger)subWithA:(NSInteger)A andB :(NSInteger)B {
    NSInteger C = A - B;
    return C;
}

@end
